#Tuple(元组)数据类型
print('-----------------------------------元组学习-----------------------------------')
#元组（tuple）与列表类似，不同之处在于元组的元素不能修改。元组写在小括号 () 里，元素之间用逗号隔开，元素可以是各种数字类型
t1=(1,2,3,4,5,6)
t2=('this','is','my')
t3=('python','folder')
print(t1,'\n',t2,'\n',t3)
#索引与字符串相似，可以进行提取、切片
print(t1[1])
print(t1[1:3])
print(t1[1:6:2])
print(t1[2:])
print(t1*2)
print(t2+t3)
print('----------------------------------------------------------------------------')